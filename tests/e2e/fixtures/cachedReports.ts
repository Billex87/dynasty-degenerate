function normalizeFixtureLeagueId(leagueId: string) {
  if (/^\d{8,24}$/.test(leagueId)) return leagueId;
  let hash = 0;
  for (let index = 0; index < leagueId.length; index += 1) {
    hash = (hash * 31 + leagueId.charCodeAt(index)) >>> 0;
  }
  return `1300${String(hash).padStart(10, '0').slice(0, 10)}`;
}

